public void validarCampos() throws InventarioException {
    
    String textoUnidades = CampoStock.getText().trim();
    String textoPrecio = CampoPrecioCompra.getText().trim();
    try {        
        //  textoPrecio = textoPrecio.replace(",", ".").trim(); 
          PrecioCompra = Double.parseDouble(textoPrecio);
          if (PrecioCompra <= 0) {
              throw new InventarioException("Precio de compra inv치lido.");
          }
      } catch (NumberFormatException e) {

          throw new InventarioException("Formato inv치lido en precio de compra.");
      }
      try {
          textoUnidades = textoUnidades.replace(",", ".");
          Unidades = Double.parseDouble(textoUnidades);
          if (Unidades <= 0) {
              throw new InventarioException("Cantidad de unidades inv치lida.");
          }
      } catch (NumberFormatException e) {
          throw new InventarioException("Formato inv치lido en unidades.");
      }
      ObtenerPrecioCompra(PrecioCompra);
      ObtenerUnidades(Unidades);
    }

    
    public void ObtenerUnidades(double Unidades){
       control.setUnidades(Unidades);
    }
    public void ObtenerPrecioCompra(double PrecioCompra){
        control.setPrecioCompra(PrecioCompra);
    }
