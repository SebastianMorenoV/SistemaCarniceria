    public void crearTablaProductosEntrada(){   
        String StringprecioCompra;
        String Total;
        String subtotal;
        String Stringimporte;
        double importe;
        double precioCompra;
        double subtotalTemporal =0.0;
        String columnasTabla[] = {"ID","Nombre","unidad", "Precio de Compra", "Importe"};
        tablaProductosEntrada.setRowHeight(25);
        DefaultTableModel modelo = new DefaultTableModel(columnasTabla, 0){
            @Override
            public boolean  isCellEditable(int fila, int columna){
                return false;
            }                    
        };
        tablaProductosEntrada.setRowSelectionAllowed(true);
        tablaProductosEntrada.setColumnSelectionAllowed(false);
        tablaProductosEntrada.getTableHeader().setReorderingAllowed(false);
        tablaProductosEntrada.getTableHeader().setResizingAllowed(false);
            for(ProductoEntradaDTO producto : control.cargarProductosEntrada()){
                precioCompra = producto.getPrecioCompra();
                StringprecioCompra = String.format("%.2f", precioCompra);
                importe = producto.getUnidad()*producto.getPrecioCompra();
                Stringimporte = String.format("%.2f", importe);
                modelo.addRow(new Object[]{
                    producto.getProductoEntrada().getCodigo(),
                    producto.getProductoEntrada().getNombre(),
                    producto.getUnidad(),
                    StringprecioCompra,
                    Stringimporte 
                    }               
                );           
                subtotalTemporal+=importe;  
            }
        tablaProductosEntrada.setModel(modelo);
        double total =subtotalTemporal;
        subtotal = String.format("%.2f", subtotalTemporal);
        Total = String.format("%.2f", total);
        campoSubtotal.setText(subtotal);
        campoTotal.setText(Total);
    }
  
